create table wm_usuarios(
usuario_id				int auto_increment primary key not null ,
usuario_nombre			varchar(100),
usuario_apellido1		varchar(100),
usuario_apellido2		varchar(100),
usuario_direccion		varchar(100),
usuario_email			varchar(100) unique,
usuario_tipo			enum('administrador','local','fan','musico'),
usuario_idioma			enum('ca','es','en'),
usuario_telefono		varchar(100),
usuario_cp				int(10),
usuario_foto			varchar(20),
usuario_contrasena		varchar(20),
usuario_lon				varchar(20),
usuario_lat				varchar(20),
usuario_recuerdame		varchar(250),
usuario_block			boolean,
usuario_login_fails		int(3),
usuario_active_ahora	boolean,
usuario_ultima_visita	date,
usuario_registro		date,
usuario_ip				int unsigned,
estilo_id				int not null,
ciudad_id				int not null,
constraint estilo_usuarios foreign key (estilo_id) references wm_estilo(estilo_id),
constraint ciudad_usuarios foreign key (ciudad_id) references wm_ciudad(ciudad_id));
